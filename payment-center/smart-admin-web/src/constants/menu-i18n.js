/**
 * 获取菜单的国际化名称
 * @param {string} menuName 菜单名称
 * @param {string} locale 当前语言
 * @returns {string} 国际化后的菜单名称
 */
export function getMenuI18nName(menuName, locale = 'en_US') {
  if (!menuName) return '';
  
  // 如果找到对应的国际化映射，返回对应语言的名称
  if (MENU_I18N_MAP[menuName]) {
    return MENU_I18N_MAP[menuName][locale] || menuName;
  }
  
  // 如果没有找到映射，返回原名称
  return menuName;
}
